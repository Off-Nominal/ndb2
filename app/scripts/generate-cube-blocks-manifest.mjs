#!/usr/bin/env node
/**
 * Emits `src/web/generated/cube-blocks.css`: sorted `@import` lines for colocated block CSS
 * (same discovery rules as the former build-web-css.mjs concat). Vite bundles this via cube-entry.css.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.join(__dirname, "..");
const WEB_ROOT = path.join(APP_ROOT, "src/web");
const GEN_DIR = path.join(WEB_ROOT, "generated");
const OUT_FILE = path.join(GEN_DIR, "cube-blocks.css");

/** @param {string} dir */
function collectCssFilesRecursive(dir) {
  /** @type {string[]} */
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) out.push(...collectCssFilesRecursive(p));
    else if (ent.isFile() && ent.name.endsWith(".css")) out.push(p);
  }
  return out;
}

function shouldIncludeBlockCss(absPath) {
  const rel = path.relative(WEB_ROOT, absPath);
  const segments = rel.split(path.sep);
  if (segments[0] === "public") return false;
  if (segments[0] === "tokens") return false;
  if (segments[0] === "styles") return false;
  if (segments[0] === "generated") return false;
  return true;
}

function main() {
  const allCss = collectCssFilesRecursive(WEB_ROOT);
  const blockSources = allCss.filter(shouldIncludeBlockCss).sort((a, b) => a.localeCompare(b));

  const lines = [];
  lines.push("/**");
  lines.push(" * Colocated block `@import` list (sorted). Generated by scripts/generate-cube-blocks-manifest.mjs.");
  lines.push(" * Do not edit by hand.");
  lines.push(" */");
  lines.push("");

  for (const abs of blockSources) {
    const relWeb = path.relative(WEB_ROOT, abs).split(path.sep).join("/");
    const relFromGen = path.relative(GEN_DIR, abs).split(path.sep).join("/");
    lines.push(`/* ndb2:block: ${relWeb} */`);
    lines.push(`@import "${relFromGen}";`);
    lines.push("");
  }

  fs.mkdirSync(GEN_DIR, { recursive: true });
  fs.writeFileSync(OUT_FILE, `${lines.join("\n").trimEnd()}\n`, "utf8");
  console.error("Wrote", path.relative(APP_ROOT, OUT_FILE), `(${blockSources.length} file(s))`);
}

main();
