#!/usr/bin/env node
/**
 * Discovers `*.client.js` under `src/web/routes/`, copies each to
 * `src/web/public/routes/<same-relative-path>` (served as `/assets/routes/...`
 * because `mountWeb` uses `express.static` on `public` at `/assets`), and generates
 * `src/web/generated/routeClientScripts.ts` (map: route subdir → script URLs).
 *
 * Route modules (e.g. `handler.tsx` or `page.tsx`) pass `clientScriptsForModule(__filename)` (see `shared/clientScriptsForModule.ts`).
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.join(__dirname, "..");
const ROUTES_DIR = path.join(APP_ROOT, "src/web/routes");
const PUBLIC_ROUTES_CLIENT = path.join(APP_ROOT, "src/web/public/routes");
const GEN_FILE = path.join(APP_ROOT, "src/web/generated/routeClientScripts.ts");

function die(msg) {
  console.error(msg);
  process.exit(1);
}

/** @param {string} dir */
function collectClientJsFilesRecursive(dir) {
  /** @type {string[]} */
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) out.push(...collectClientJsFilesRecursive(p));
    else if (ent.isFile() && ent.name.endsWith(".client.js")) out.push(p);
  }
  return out;
}

function main() {
  if (!fs.existsSync(ROUTES_DIR)) die(`Missing routes dir: ${ROUTES_DIR}`);

  fs.mkdirSync(path.dirname(GEN_FILE), { recursive: true });
  fs.rmSync(PUBLIC_ROUTES_CLIENT, { recursive: true, force: true });

  const absFiles = collectClientJsFilesRecursive(ROUTES_DIR).sort((a, b) =>
    a.localeCompare(b),
  );

  /** @type {Map<string, string[]>} */
  const byDir = new Map();

  for (const abs of absFiles) {
    const relFromRoutes = path.relative(ROUTES_DIR, abs).split(path.sep).join("/");
    const dest = path.join(PUBLIC_ROUTES_CLIENT, relFromRoutes);
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(abs, dest);
    console.error("Copied", path.relative(APP_ROOT, dest));

    const dirKey = path.posix.dirname(relFromRoutes);
    const urlPath = `/assets/routes/${relFromRoutes}`;
    const list = byDir.get(dirKey) ?? [];
    list.push(urlPath);
    byDir.set(dirKey, list);
  }

  const keys = [...byDir.keys()].sort((a, b) => a.localeCompare(b));
  const lines = [];
  lines.push("/**");
  lines.push(" * Route colocated client scripts → `/assets/routes/...` URLs.");
  lines.push(" * Generated by scripts/build-client-js.mjs — do not edit by hand.");
  lines.push(" */");
  lines.push("");
  lines.push("export const routeClientScripts = {");

  for (const k of keys) {
    const urls = byDir.get(k);
    if (!urls) continue;
    const inner = urls.map((u) => JSON.stringify(u)).join(", ");
    lines.push(`  ${JSON.stringify(k)}: [${inner}] as const,`);
  }

  lines.push("} as const;");
  lines.push("");
  lines.push("export type RouteClientScriptBundleKey = keyof typeof routeClientScripts;");
  lines.push("");
  lines.push(
    "export function clientScriptsForRouteDir(dir: string): readonly string[] {",
  );
  lines.push(
    "  const map = routeClientScripts as Record<string, readonly string[]>;",
  );
  lines.push("  return map[dir] ?? [];");
  lines.push("}");
  lines.push("");

  fs.writeFileSync(GEN_FILE, lines.join("\n"), "utf8");
  console.error("Wrote", path.relative(APP_ROOT, GEN_FILE));
}

main();
